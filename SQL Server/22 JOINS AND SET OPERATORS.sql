use db7am

--CREATING TWO TABLES TO PERFORM JOIN AND UNIONS--
CREATE TABLE T1 (F1 INT) INSERT INTO T1 VALUES(1), (2), (3), (10), (20), (30)

CREATE TABLE T2 (F1 INT) INSERT INTO T2 VALUES(1), (2), (3), (40), (50), (60)

-- EQUI JOIN--
SELECT T1.F1 AS TABLE1, T2.F1 AS TABLE2 FROM T1 INNER JOIN T2 ON T1.F1=T2.F1

-- LEFT JOIN--
SELECT T1.F1 AS TABLE1, T2.F1 AS TABLE2 FROM T1 LEFT JOIN T2 ON T1.F1=T2.F1

-- RIGHT JOIN--
SELECT T1.F1 AS TABLE1, T2.F1 AS TABLE2 FROM T1 RIGHT JOIN T2 ON T1.F1=T2.F1

-- FULL JOIN--
SELECT T1.F1 AS TABLE1, T2.F1 AS TABLE2 FROM T1 FULL JOIN T2 ON T1.F1=T2.F1

-- UNION--
SELECT F1 FROM T1
UNION
SELECT F1 FROM T2

-- UNIONALL--
SELECT F1 FROM T1
UNION ALL
SELECT F1 FROM T2

-- INTERSECT--
SELECT F1 FROM T1
INTERSECT
SELECT F1 FROM T2

-- EXCEPT--
SELECT F1 FROM T1
EXCEPT
SELECT F1 FROM T2

SELECT F1 FROM T2
EXCEPT
SELECT F1 FROM T1